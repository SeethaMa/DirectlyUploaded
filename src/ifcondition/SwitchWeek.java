package ifcondition;

public class SwitchWeek {
	public static void main (String[]args) {
	String day="Monday";
	switch (day) {
	case "Sunday":System.out.println(day+" is first day of week");
	break;
	case "Monday":System.out.println(day+" is 2nd day of week");
	break;
	case "Thesday":System.out.println(day+" is 3rd day of week");
	break;
	case "Wednesday":System.out.println(day+" is 4th day of week");
	break;
	case "Thurday":System.out.println(day+" is 5th day of week");
	break;
	case "Friday":System.out.println(day+" is 6th day of week");
	break;
	case "Saturday":System.out.println(day+" is 7th day of week");
	break;
	
	}
	}
}
